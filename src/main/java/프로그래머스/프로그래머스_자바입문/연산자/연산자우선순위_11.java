package 프로그래머스.프로그래머스_자바입문.연산자;
//and 연산자가 or보다 우선순위가 높음
public class 연산자우선순위_11 {
    public static void main(String[] args){
        int a = 5;
        int b = 10;
        int c = 15;

        System.out.println((a-b)*c);
        System.out.println(a>5 && b>5);
        System.out.println(a>5 || b>5);
        System.out.println(a - 5);




    }
}
