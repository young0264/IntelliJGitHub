package 프로그래머스.프로그래머스_자바입문.연산자;

public class 비교연산자_10 {  // 비교연산자 & 대입연산자
    public static void main(String[] args){
        int i = 10; //단순대입 연산자
        int j = 10;

        System.out.println(i == j);
        System.out.println(i != j);
        System.out.println(i < j);
        System.out.println(i >= j);
        System.out.println(i <= j);
        System.out.println(i < j);


    }
}
